package Basic_Proggrams;

public class String_concatination {
	public static void main(String args[]){
		
		int i=20;
		int j=40;
		System.out.println(i+j);
		
		String c="Hello";
		String b="World";
		System.out.println(i+j+c+b);
		System.out.println(i+c+j+b);
		System.out.println(c+b+i+j);
		System.out.println(c+b+(i+j));
		System.out.print(i);
	}

}
